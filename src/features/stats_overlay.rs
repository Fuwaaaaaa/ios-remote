/// Stats overlay: render FPS, latency, resolution info on top of the frame.
///
/// Drawn as a semi-transparent bar at the top of the display window. Also
/// hosts the project's minimal 5x7 bitmap font, reused by the subtitle
/// renderer in [`super::audio_transcription`].

#[derive(Clone, Debug)]
pub struct Stats {
    pub fps: f64,
    pub frame_count: u64,
    pub latency_ms: f64,
    pub resolution: (u32, u32),
    pub bitrate_kbps: f64,
}

/// Cyan: stats overlay default.
pub const COLOR_CYAN: [u8; 3] = [0x00, 0xD4, 0xFF];
/// White: subtitle default.
pub const COLOR_WHITE: [u8; 3] = [0xF8, 0xF8, 0xF8];

/// Draw stats text onto an RGBA buffer (simple bitmap font).
pub fn draw_stats_overlay(rgba: &mut [u8], width: u32, _height: u32, stats: &Stats) {
    let text = format!(
        " {:.0} FPS | {}x{} | {:.0}ms | {:.0} kbps | {} frames ",
        stats.fps,
        stats.resolution.0,
        stats.resolution.1,
        stats.latency_ms,
        stats.bitrate_kbps,
        stats.frame_count,
    );

    // Draw semi-transparent dark bar at top (24px tall)
    let bar_height = 24u32;
    for y in 0..bar_height.min(_height) {
        for x in 0..width {
            let idx = ((y * width + x) * 4) as usize;
            if idx + 3 < rgba.len() {
                rgba[idx] = (rgba[idx] as u16 * 40 / 100) as u8;
                rgba[idx + 1] = (rgba[idx + 1] as u16 * 40 / 100) as u8;
                rgba[idx + 2] = (rgba[idx + 2] as u16 * 40 / 100) as u8;
            }
        }
    }

    draw_text(rgba, width, 8, 8, &text, COLOR_CYAN);
}

/// Draw a string at (`x`, `y`) using the 5x7 bitmap font, advancing 6px per
/// character. Lines are clipped against the buffer width; callers wrap.
pub fn draw_text(rgba: &mut [u8], width: u32, x: u32, y: u32, text: &str, color: [u8; 3]) {
    let mut cx = x;
    for ch in text.chars() {
        if cx + 5 >= width {
            break;
        }
        draw_char(rgba, width, cx, y, ch, color);
        cx += 6;
    }
}

/// Draw a single character using the 5x7 bitmap font.
pub fn draw_char(rgba: &mut [u8], width: u32, x: u32, y: u32, ch: char, color: [u8; 3]) {
    let bitmap = char_bitmap(ch);
    for row in 0..7u32 {
        for col in 0..5u32 {
            if (bitmap[row as usize] >> (4 - col)) & 1 == 1 {
                let px = x + col;
                let py = y + row;
                let idx = ((py * width + px) * 4) as usize;
                if idx + 2 < rgba.len() {
                    rgba[idx] = color[0];
                    rgba[idx + 1] = color[1];
                    rgba[idx + 2] = color[2];
                }
            }
        }
    }
}

